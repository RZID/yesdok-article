export const actions = {
  async getArticlesByCategory(context, category) {
    return new Promise((resolve, reject) => {
      let articles = []
      for (let el of context.state.articles) {
        if (el.category.link.toLowerCase() === category.toLowerCase()) {
          articles.push(el)
        }
      }
      articles.length > 0 ? resolve(articles) : reject('Not found')
    })
  },

  async getArticleByLink(context, link) {
    return new Promise(async (resolve, reject) => {
      let article = null
      for (let el of context.state.articles) {
        if (el.link === link) {
          article = el
        }
      }

      if (article) {
        resolve(article)
      } else {
        reject('Not found')
      }
    })
  },
}

export const getters = {
  getArticles(state) {
    return state.articles
  },
  getCategory(state) {
    return state.articleCategories
  },
}
